class Solution {
    fun climbStairs(n: Int): Int {
        return solution2(n)
    }

    fun solution2(n: Int): Int {
        var dp = arrayOfNulls<Int>(n + 1)
        dp[0] = 1
        dp[1] = 1
        for (i in 2..n) {
            dp[i] = dp[i - 1]?.plus(dp[i - 2]!!)
        }

        return dp[n]!!
    }

    // é€’å½’ï¼Œè¶…æ—¶
    fun solution1(n: Int): Int {
        if (n == 1) {
            return 1
        } else if (n == 2) {
            return 2
        } else {
            return solution1(n - 1) + solution1(n - 2)
        }
    }
}
