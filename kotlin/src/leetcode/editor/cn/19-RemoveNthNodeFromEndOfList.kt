/**
 * Example:
 * var li = ListNode(5)
 * var v = li.`val`
 * Definition for singly-linked list.
 * class ListNode(var `val`: Int) {
 *     var next: ListNode? = null
 * }
 */
class Solution {
    fun removeNthFromEnd(head: ListNode?, n: Int): ListNode? {
        val pre = ListNode(0)
        pre.next = head
        var p: ListNode? = pre
        var q: ListNode? = pre
        var count = n
        while (count > 0) {
            q = q?.next
            count -= 1
        }
        while (q?.next != null) {
            p = p?.next
            q = q.next
        }

        p?.next = p?.next?.next
        return pre.next
    }
}
