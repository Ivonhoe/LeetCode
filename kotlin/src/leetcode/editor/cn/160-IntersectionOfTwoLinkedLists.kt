/**
 * Example:
 * var li = ListNode(5)
 * var v = li.`val`
 * Definition for singly-linked list.
 * class ListNode(var `val`: Int) {
 *     var next: ListNode? = null
 * }
 */

class Solution {
    fun getIntersectionNode(headA: ListNode?, headB: ListNode?): ListNode? {
        return solution2(headA, headB)
    }

    fun solution2(headA: ListNode?, headB: ListNode?): ListNode? {
        var pA = headA
        var pB = headB
        while (pA != pB) {
            if (pA == null) {
                pA = headB
            } else {
                pA = pA.next
            }

            if (pB == null) {
                pB = headA
            } else {
                pB = pB.next
            }
        }

        return pA
    }

    // æŒ‡é’ˆéåŽ†
    fun solution1(headA: ListNode?, headB: ListNode?): ListNode? {
        var lenghtA = listLength(headA)
        var lenghtB = listLength(headB)

        var a = headA
        var b = headB
        var direction = 1
        if (lenghtA > lenghtB) {
            direction = 1
        } else {
            direction = -1
            a = headB
            b = headA
        }
        var count = (lenghtA - lenghtB) * direction

        while (count > 0) {
            count = count - 1
            a = a?.next
        }

        while (a != null && b != null) {
            if (a == b) {
                return a
            } else {
                a = a!!.next
                b = b!!.next
            }
        }
        return null
    }

    fun listLength(head: ListNode?): Int {
        if (head == null) {
            return 0
        }

        var count = 0
        var p = head
        while (p != null) {
            count++
            p = p.next
        }
        return count
    }
}
