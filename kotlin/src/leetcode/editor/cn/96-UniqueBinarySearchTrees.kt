class Solution {
    // G(n)=G(0)âˆ—G(nâˆ’1)+G(1)âˆ—(nâˆ’2)+...+G(nâˆ’1)âˆ—G(0)
    fun numTrees(n: Int): Int {
        var dp = Array<Int>(n + 1) { 0 }
        dp[0] = 1
        dp[1] = 1
        for (i in 2..n) {
            for (j in 1..i) {
                dp[i] += dp[j - 1] * dp[i - j]
            }
        }

        return dp[n]
    }
}
