/**
 * Example:
 * var ti = TreeNode(5)
 * var v = ti.`val`
 * Definition for a binary tree node.
 * class TreeNode(var `val`: Int) {
 *     var left: TreeNode? = null
 *     var right: TreeNode? = null
 * }
 */
class Solution {
    fun kthLargest(root: TreeNode?, k: Int): Int {
        var res = 0
        var count = k
        fun dfs(r: TreeNode?) {
            if (r == null) return
            dfs(r.right)
            count = count.minus(1)
            if (count == 0) {
                res = r.`val`
                return
            }
            dfs(r.left)
        }
        dfs(root)
        return res
    }
}
