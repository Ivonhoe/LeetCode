// @lc code=start
/**
 * Definition for a binary tree node.
 * function TreeNode(val) {
 *     this.val = val;
 *     this.left = this.right = null;
 * }
 */
/**
 * @param {TreeNode} root
 * @return {number[][]}
 */
var levelOrder = function (root) {
    if (!root) {
        return []
    }

    var result = []
    var queue = []
    queue.push(root)
    while (queue.length) {
        var list = []
        var length = queue.length;
        for (i = 0; i < length; i++) {
            var node = queue.shift()
            list.push(node.val)
            if (node.left) {
                queue.push(node.left)
            }
            if (node.right) {
                queue.push(node.right)
            }
        }
        result.push(list)
    }

    return result
};
